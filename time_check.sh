   vlc --fullscreen --effect-list=spectrometer foo.wav
#espeak -g10 -s180 "$f Hour"
#espeak -g10 -s180 "$f1 minute"
espeak -g10 -s180 " $f Hour $f1 minute $f2 second" -w foo.wav
   vlc --fullscreen --effect-list=spectrometer foo.wav
   sleep 1
echo " ---------------------------------------------------------------------"
echo " Time : $f : $f1 :$f2 $f3 "
